package saskes;

public class saske {
	
	int checkerX;
	int checkerY;
	
	saske(int x, int y){
		this.checkerX=x;
		this.checkerY=y;
	}

	public int getCheckerX() {
		return checkerX;
	}

	public void setCheckerX(int checkerX) {
		this.checkerX = checkerX;
	}

	public int getCheckerY() {
		return checkerY;
	}

	public void setCheckerY(int checkerY) {
		this.checkerY = checkerY;
	}
	
	

}
